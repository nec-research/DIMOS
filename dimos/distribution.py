class DiscretizedDistribution(torch.nn.Module):
    """
    Discretized version of a continuous distribution.

    This class creates a discrete distribution from a continuous one by defining
    the probability mass function as the difference between CDFs:
        p(x) = phi(x+1) - phi(x)
    where phi is the CDF of the original distribution.

    Parameters
    ----------
    base_distribution : ContinuousDistribution
        The continuous distribution to discretize
    """

    def __init__(self, base_distribution):
        super().__init__()
        self.base_distribution = base_distribution

    def to(self, device):
        """
        Moves the distribution to the specified device.

        Parameters
        ----------
        device : torch.device
            The device to move to
        """
        super().to(device)
        self.device = device
        self.base_distribution.to(device)

    def log_prob(self, value: torch.Tensor) -> torch.Tensor:
        """
        Computes the log probability mass function of the discretized distribution.

        Parameters
        ----------
        value : torch.Tensor
            Input values of shape (N,1) where N is the number of samples

        Returns
        -------
        torch.Tensor
            Log probabilities of shape (N,1)
        """
        # avoids a degenerate case where the base distribution has the
        # same cdf for both value and value+1, which leads to nan.

        return torch.log(
            self.base_distribution.cdf(value + 1.0)
            - self.base_distribution.cdf(value)
            + 1e-6
        )

    def cdf(self, value: torch.Tensor) -> torch.Tensor:
        """
        Computes the cumulative distribution function of the discretized distribution.

        Parameters
        ----------
        value : torch.Tensor
            Input values of shape (N,1) where N is the number of samples

        Returns
        -------
        torch.Tensor
            CDF values of shape (N,1)
        """
        return self.base_distribution.cdf(value + 1.0)

    def quantile(self, p: float = 0.99):
        """
        Computes the approximated p-quantile for the discrete distribution.
        Returns the smallest integer x such that cdf(x) >= p.

        Parameters
        ----------
        p : float, optional
            The probability level for the quantile, default is 0.99

        Returns
        -------
        tuple
            The quantile value and probabilities up to that value
        """
        upper_bound = self.base_distribution._quantile_upper_bound(p)
        cutoffs = torch.arange(0, upper_bound.item(), 1, dtype=torch.int32)  # shape [N]
        
        probs = self.log_prob(cutoffs).exp()
        cumsum = torch.cumsum(probs, dim=0)  
        quantile = torch.searchsorted(cumsum, p)

        return quantile, probs[:quantile]

    def compute_probability_vector(self, x) -> torch.Tensor:
        """
        Computes the renormalized vector of probabilities using logsumexp for numerical stability.

        Parameters
        ----------
        x : torch.Tensor
            Input values

        Returns
        -------
        torch.Tensor
            Vector of probabilities summing to 1
        """
        log_probs = self.log_prob(x).squeeze(1)
        log_sum = torch.logsumexp(log_probs, dim=0)
        probs = (log_probs - log_sum).exp()
        return probs

    @property
    def mean(self) -> torch.Tensor:
        """Mean of the distribution"""
        return self.base_distribution.mean

    @property
    def variance(self) -> torch.Tensor:
        """Variance of the distribution"""
        return self.base_distribution.variance

    def get_parameters(self):
        """Returns the distribution parameters"""
        return self.base_distribution.get_parameters()


class TruncatedDistribution(torch.nn.Module):
    """
    Truncated version of a discretized distribution.

    This class truncates a discretized distribution at a given quantile and
    renormalizes the probabilities.

    Parameters
    ----------
    truncation_quantile : float
        The quantile in [0,1] at which to truncate
    base_distribution : ContinuousDistribution
        The base continuous distribution to discretize and truncate
    """

    def __init__(self, truncation_quantile: float, base_distribution):
        super().__init__()

        self.truncation_quantile = truncation_quantile
        self.discretized_distribution = DiscretizedDistribution(base_distribution)

    def to(self, device):
        """
        Moves the distribution to the specified device.

        Parameters
        ----------
        device : torch.device
            The device to move to
        """
        super().to(device)
        self.device = device
        self.discretized_distribution.to(device)

    def compute_truncation(self) -> int:
        """
        Computes the truncation number at the specified quantile.

        Returns
        -------
        int
            The truncation number
        """
        truncation_number, _ = self.discretized_distribution.quantile(
            p=self.truncation_quantile
        )

        # detach: this must not be part of the gradient computation in any way
        truncation_number = int(truncation_number.detach())

        assert truncation_number > 0
        return truncation_number

    def compute_weights(self) -> torch.Tensor:
        """
        Computes the renormalized vector of probabilities.

        Returns
        -------
        torch.Tensor
            Vector of probabilities summing to 1
        """
        _, probs = self.discretized_distribution.quantile()
        return probs/probs.sum()

    def compute_truncation_and_weights(self):
        """
        Computes both truncation number and renormalized weights.

        Returns
        -------
        tuple
            The truncation number and vector of probabilities
        """
        quantile, probs = self.discretized_distribution.quantile()
        return int(quantile.detach()), probs/probs.sum()

    @property
    def mean(self) -> torch.Tensor:
        """Mean of the truncated distribution"""
        proba = self.compute_probability_vector()
        return (proba * torch.arange(len(proba)).to(proba.device)).sum()

    def get_parameters(self):
        """Returns the distribution parameters"""
        return self.discretized_distribution.get_parameters()


class FoldedNormal(ContinuousDistribution):
    """
    Implementation of the folded normal distribution.

    This distribution arises when taking the absolute value of a normal random variable:
        X ~ Normal(loc, scale)
        Y = |X| ~ FoldedNormal(loc, scale)

    Parameters
    ----------
    loc : float
        Location parameter (must be >= 0)
    scale : float
        Scale parameter
    """

    def __init__(self, loc: float, scale: float):
        super(FoldedNormal, self).__init__()

        assert loc >= 0.0, (
            "expected loc >=0 for our work and for a correct quantile" " computation"
        )
        self.base_loc = torch.tensor([loc])
        self._base_scale = torch.tensor([scale])

    @property
    def base_scale(self) -> torch.Tensor:
        """Scale parameter with softplus transformation"""
        return torch.nn.functional.softplus(self._base_scale) + 0.5  # const

    @property
    def mean(self) -> torch.Tensor:
        """
        Mean of the folded normal distribution.

        Returns
        -------
        torch.Tensor
            The mean value
        """
        mu_squared = torch.pow(self.base_loc, 2.0)
        sigma_squared = torch.pow(self.base_scale, 2.0)

        mean = self.base_scale * torch.sqrt(torch.tensor([2.0]) / torch.pi) * torch.exp(-0.5 * (mu_squared / sigma_squared)) \
            + self.base_loc * (1.0 - (1.0 + torch.erf(-torch.tensor([self.base_loc]) / torch.tensor([self.base_scale]))))
        return mean

    @property
    def variance(self) -> torch.Tensor:
        """
        Variance of the folded normal distribution.

        Returns
        -------
        torch.Tensor
            The variance value
        """
        mu_squared = torch.pow(self.base_loc, 2.0)
        sigma_squared = torch.pow(self.base_scale, 2.0)

        # add a minimal variance to avoid degenerate cases
        return mu_squared + sigma_squared - torch.pow(self.mean, 2.0)

    def log_prob(self, value) -> torch.Tensor:
        """
        Computes the log probability density function.

        Parameters
        ----------
        value : torch.Tensor
            Input values of shape (N,1)

        Returns
        -------
        torch.Tensor
            Log probabilities of shape (N,1)
        """
        base_dist_1 = torch.distributions.Normal(self.base_loc, self.base_scale)
        base_dist_2 = torch.distributions.Normal(-self.base_loc, self.base_scale)

        log_prob = torch.logsumexp(
            torch.stack((base_dist_1.log_prob(value), base_dist_2.log_prob(value)), dim=-1),
            dim=-1)

        # deal with cases where x <= 0
        log_prob = torch.where(value >= 0, log_prob, -torch.inf)

        return log_prob

    def cdf(self, value):
        """
        Computes the cumulative distribution function.

        Parameters
        ----------
        value : torch.Tensor
            Input values of shape (N,1)

        Returns
        -------
        torch.Tensor
            CDF values of shape (N,1)
        """
        base_dist_1 = torch.distributions.Normal(self.base_loc + 1e-4, self.base_scale)
        base_dist_2 = torch.distributions.Normal(-self.base_loc - 1e-4, self.base_scale)

        term_1 = (2.0 * base_dist_1.cdf(value)) - 1.0
        term_2 = (2.0 * base_dist_2.cdf(value)) - 1.0
        cdf = 0.5 * (term_1 + term_2)

        # deal with cases where x <= 0
        cdf = torch.where(value >= 0, cdf, 0)

        return cdf

    def _quantile_lower_bound(self, p: float = 0.99) -> torch.Tensor:
        """
        Computes lower bound for the p-quantile.

        Since CDF of normal is always >= CDF of folded normal, any p-quantile of
        normal is <= p-quantile of the folded normal.

        Parameters
        ----------
        p : float, optional
            The probability level, default is 0.99

        Returns
        -------
        torch.Tensor
            Lower bound for the p-quantile
        """
        p = torch.tensor([p])
        normal_quantile = self.base_loc + self.base_scale * torch.sqrt(torch.tensor([2.0])) * torch.erfinv(2.0 * p - 1.0)

        # if normal quantile is x < 0, then it becomes x'=0 in a folded normal
        # but we require mu > 0 so it should not be a problem
        return torch.relu(normal_quantile)

    def _quantile_upper_bound(self, p: float = 0.99) -> torch.Tensor:
        """
        Computes upper bound for the p-quantile using Chernoff's bound.

        Parameters
        ----------
        p : float, optional
            The probability level, default is 0.99

        Returns
        -------
        torch.Tensor
            Upper bound for the p-quantile
        """
        p = torch.tensor([p])

        def normal_cdf(x):
            return 0.5 * (1 + torch.erf(x / torch.sqrt(torch.tensor([2.0]))))

        mu_by_sigma = self.base_loc / self.base_scale
        const = torch.exp(torch.tensor([0.5])) * (
            normal_cdf(1 + mu_by_sigma)
            + (normal_cdf(1 - mu_by_sigma) / torch.exp(2 * mu_by_sigma))
        )

        p_quantile_upper_bound = (
            self.base_loc + self.base_scale * torch.log(const) - self.base_scale * torch.log(1.0 - p)
        )
        return p_quantile_upper_bound

    def get_parameters(self):
        """Returns the distribution parameters"""
        return [self.base_loc, self._base_scale]
