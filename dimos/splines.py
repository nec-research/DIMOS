"""
This module provides spline interpolation functionality in one and two dimensions.

The module includes classes for handling one-dimensional and two-dimensional spline
interpolation, as well as B-spline functions. The main classes are:

- OneDimensionalSpline: Handles one-dimensional spline interpolation with methods
  for fitting, transforming, and evaluating splines and their derivatives.

- TwoDimensionalSpline: Handles two-dimensional spline interpolation with similar
  functionality to the one-dimensional case but extended to two dimensions.

- _BSplineFn: Implements B-spline basis functions using PyTorch's autograd for
  automatic differentiation.

The splines implemented here are cubic splines with optional periodic boundary
conditions. The implementation uses tridiagonal matrix solvers for efficiency
where applicable.
"""

import torch


class OneDimensionalSpline():
    """
    Class to handle one-dimensional spline interpolation.

    Methods
    -------
    solve_tridigonal_matrix(a, b, c, rhs)
        Solves a tridiagonal matrix system.
    fit(grid, x, **kargs)
        Fits the spline to the given data.
    transform(x, grad=False)
        Transforms the input data using the fitted spline.
    fit_and_transform(grid, x, y, grad=False, **kargs)
        Fits the spline to the given data and transforms the input data.
    __call__(x, grad=False)
        Calls the transform method.
    initialize_spline(grid, data, periodic=False, use_tridiagonal=True)
        Initializes the spline with the given grid and data.
    evaluate_spline(position)
        Evaluates the spline at the given position.
    evaluate_spline_derivative(position)
        Evaluates the derivative of the spline at the given position.
    evaluate_spline_separate(position)
        Evaluates the spline at the given position using a separate method.
    evaluate_spline_derivative_separate(position)
        Evaluates the derivative of the spline at the given position using a separate method.
    initialize_and_evaluate_derivative(grid, data, position, periodic=False, use_tridiagonal=True)
        Initializes the spline and evaluates its derivative at the given position.
    """

    def __init__(self):
        """
        Initializes the OneDimensionalSpline class.
        """
        pass

    def solve_tridigonal_matrix(self, a, b, c, rhs):
        """
        Solves a tridiagonal matrix system.

        Parameters
        ----------
        a : torch.Tensor
            Sub-diagonal elements of the tridiagonal matrix.
        b : torch.Tensor
            Diagonal elements of the tridiagonal matrix.
        c : torch.Tensor
            Super-diagonal elements of the tridiagonal matrix.
        rhs : torch.Tensor
            Right-hand side of the equation.

        Returns
        -------
        torch.Tensor
            Solution of the tridiagonal matrix system.
        """
        solution = torch.zeros_like(a)
        gamma = torch.zeros_like(a)

        solution[0] = rhs[0] / b[0]
        beta = b[0]

        gamma[1:] = c[:-1] / beta
        for i in range(1, a.size(0)):
            gamma[i] = c[i - 1] / beta
            beta = b[i] - a[i] * gamma[i]
            solution[i] = (rhs[i] - a[i] * solution[i - 1]) / beta

        for i in range(a.size(0) - 2, 0, -1):
            solution[i] = solution[i] - gamma[i + 1] * solution[i + 1]
        return solution

    @torch.compiler.disable(recursive=True)
    def initialize_spline(self, grid, data, periodic=False, use_tridiagonal=True):
        """
        Initializes the spline with the given grid and data.

        Parameters
        ----------
        grid : torch.Tensor
            Grid points for the spline.
        data : torch.Tensor
            Data points for the spline.
        periodic : bool, optional
            If True, the spline is periodic. Default is False.
        use_tridiagonal : bool, optional
            If True, uses a tridiagonal matrix solver. Default is True.

        Returns
        -------
        None
        """
        assert grid.dim() == 1, "Data is not one dimensional"
        length = grid.size(0)
        self.data = data
        self.grid = grid

        if periodic:
            self.derivative = torch.zeros((length))
            a = torch.zeros((length - 1))
            b = torch.zeros((length - 1))
            c = torch.zeros((length - 1))
            rhs = torch.zeros((length - 1))

            a[0] = grid[-1] - grid[-2]
            c[0] = grid[1] - grid[0]
            b[0] = 2.0 * (c[0] + a[0])
            rhs[0] = 6.0 * ((data[1] - data[0]) / c[0] - (data[-1] - data[-2]) / a[0])

            a[1:] = grid[1:-1] - grid[:-2]
            b[1:] = 2.0 * (grid[2:] - grid[:-2])
            c[1:] = grid[2:] - grid[1:-1]
            rhs[1:] = 6.0 * ((data[2:] - data[1:-1]) / c[1:] - (data[1:-1] - data[:-2]) / a[1:])

            beta = a[0]
            alpha = c[-2]
            gamma = -b[0]

            # This is apparently the Sherman-Morrison method method, see OpenMM
            length = length - 1
            b[0] = b[0] - gamma
            b[-1] = b[-1] - alpha * beta / gamma
            deriv = self.solve_tridigonal_matrix(a, b, c, rhs)
            u = torch.zeros((length))
            z = self.solve_tridigonal_matrix(a, b, c, u)
            scale = (deriv[0] + beta * deriv[-1] / gamma) / (1.0 + z[0] + beta * z[-1] / gamma)
            self.derivative[:-1] = deriv - scale * z
            self.derivative[-1] = deriv[0]
        else:
            a = torch.zeros((length))
            b = torch.ones((length))
            c = torch.zeros((length))
            rhs = torch.zeros((length))

            a[1:-1] = grid[1:-1] - grid[:-2]
            b[1:-1] = 2.0 * (grid[2:] - grid[:-2])
            c[1:-1] = grid[2:] - grid[1:-1]
            rhs[1:-1] = 6.0 * ((data[2:] - data[1:-1]) / c[1:-1] - (data[1:-1] - data[:-2]) / a[1:-1])

            if use_tridiagonal:
                self.derivative = self.solve_tridigonal_matrix(a, b, c, rhs)
            else:
                self.matrix = torch.zeros((length, length))
                self.matrix = torch.diagonal_scatter(self.matrix, a[1:], offset=-1)
                self.matrix = torch.diagonal_scatter(self.matrix, b, offset=0)
                self.matrix = torch.diagonal_scatter(self.matrix, c[:-1], offset=1)
                self.derivative = torch.linalg.solve(self.matrix, rhs)

    @torch.compiler.disable(recursive=True)
    def evaluate_spline(self, position):
        """
        Evaluates the spline at the given position.

        Parameters
        ----------
        position : torch.Tensor
            Position at which to evaluate the spline.

        Returns
        -------
        torch.Tensor
            Evaluated spline value at the given position.
        """
        upper = torch.searchsorted(self.grid, position, right=False)
        lower = upper - 1

        mask = lower < 0
        upper[mask] = upper[mask] + 1
        lower[mask] = lower[mask] + 1

        dx = self.grid[upper] - self.grid[lower]
        a = (self.grid[upper] - position) / dx
        b = 1.0 - a
        return a * self.data[lower] + b * self.data[upper] + \
            ((a * a * a - a) * self.derivative[lower] + (b * b * b - b) * self.derivative[upper]) * dx * dx / 6.0

    @torch.compiler.disable(recursive=True)
    def evaluate_spline_derivative(self, position):
        """
        Evaluates the derivative of the spline at the given position.

        Parameters
        ----------
        position : torch.Tensor
            Position at which to evaluate the derivative of the spline.

        Returns
        -------
        torch.Tensor
            Evaluated derivative of the spline at the given position.
        """
        upper = torch.searchsorted(self.grid, position, right=False)
        lower = upper - 1

        mask = lower < 0
        upper[mask] = upper[mask] + 1
        lower[mask] = lower[mask] + 1

        dx = self.grid[upper] - self.grid[lower]
        a = (self.grid[upper] - position) / dx
        b = 1.0 - a
        dadx = -1.0 / dx

        return dadx * self.data[lower] - dadx * self.data[upper] + \
            ((1.0 - 3.0 * a * a) * self.derivative[lower] + (3.0 * b * b - 1.0) * self.derivative[upper]) * dx / 6.0

    @torch.compiler.disable(recursive=True)
    def initialize_and_evaluate_derivative(self, grid, data, position, periodic=False, use_tridiagonal=True):
        """
        Initializes the spline and evaluates its derivative at the given position.

        Parameters
        ----------
        grid : torch.Tensor
            Grid points for the spline.
        data : torch.Tensor
            Data points for the spline.
        position : torch.Tensor
            Position at which to evaluate the derivative of the spline.
        periodic : bool, optional
            If True, the spline is periodic. Default is False.
        use_tridiagonal : bool, optional
            If True, uses a tridiagonal matrix solver. Default is True.

        Returns
        -------
        torch.Tensor
            Evaluated derivative of the spline at the given position.
        """
        result = torch.empty_like(data)
