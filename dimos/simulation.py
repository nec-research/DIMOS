"""
Simulation module for MD and MC simulation objects.

This module provides base classes and implementations for molecular simulations,
including both Molecular Dynamics (MD) and Monte Carlo (MC) methods. It defines
the core simulation classes and interfaces that specific simulation types
build upon.

The module contains the following main classes:

- Simulation: Abstract base class defining common simulation functionality
- MDSimulation: Implementation of Molecular Dynamics simulations 
- MCSimulation: Implementation of Monte Carlo simulations

These classes handle internally:
- System configuration management (positions, velocities, neighbor lists)
- Energy calculations and minimization
- Integration of equations of motion (MD)
- Move selection and acceptance/rejection (MC)
- Temperature and pressure control
- Periodic boundary conditions
"""

import torch

from dimos.utils import maxwell_boltzmann_distr, kinetic_energy
from dimos.neighbor_handling import NeighborHandling


class Simulation():
    """
    Base class for molecular simulations.
    
    This abstract class provides the foundation for different types of molecular simulations
    such as Molecular Dynamics (MD) and Monte Carlo (MC). It defines common functionality
    for energy calculations and minimization.
    
    Attributes
    ----------
    sys : object
        The system object containing information about the molecular system.
    pos : torch.Tensor
        Tensor containing the current positions of the atoms.
    neighborlist : object or None
        Object containing information about neighboring atoms, used for efficient
        force and energy calculations.
    neighbor_handling : NeighborHandling or None
        Object responsible for managing neighbor lists.
    """

    def step(self):
        """
        Perform a single simulation step.
        
        This method must be implemented by subclasses.
        
        Raises
        ------
        NotImplementedError
            If the subclass does not implement this method.
        """
        raise NotImplementedError("Step needs to be implemented by the respective simulations.")

    def measure_potential_energy(self):
        """
        Calculate the potential energy of the current configuration.
        
        Returns
        -------
        torch.Tensor
            The potential energy of the system.
        """
        self.neighborlist = self.neighbor_handling.check_recalc(self.pos)
        return self.sys.calc_energy(self.pos, self.neighborlist)

    def minimize_energy(self, num_optim, print_details=False, optimizer="LBFGS"):
        """
        Minimize the energy of the system using gradient-based optimization.
        
        Parameters
        ----------
        num_optim : int
            Number of optimization steps to perform.
        print_details : bool, optional
            Whether to print energy at each step. Default is False.
        optimizer : str or torch.optim.Optimizer, optional
            The optimizer to use. Can be "LBFGS", "SGD"/"GD", "Adam", or a PyTorch optimizer
            class. Default is "LBFGS".
            
        Raises
        ------
        NotImplementedError
            If an unsupported optimizer string is provided.
        """
        if self.sys.periodic:
            self.pos = self.sys.apply_boundary_conditions(self.pos)
        self.pos.detach_().requires_grad_()
        if isinstance(optimizer, str):
            type = optimizer.lower()
            match type:
                case "lbfgs":
                    def closure():
                        optimizer.zero_grad()
                        energy = self.sys.calc_energy(self.pos, self.neighborlist)
                        if self.sys.use_neighborlist:
                            self.neighborlist = self.neighbor_handling.check_recalc(self.pos)
                        energy.backward()
                        return energy
                    optimizer = torch.optim.LBFGS([self.pos])
                    for optim_step in range(num_optim):
                        energy = optimizer.step(closure)
                        if print_details:
                            print(optim_step, energy.item())
                    return
                case "sgd" | "gd":
                    optimizer = torch.optim.SGD([self.pos], lr=0.0001)
                case "adam":
                    optimizer = torch.optim.Adam([self.pos], lr=0.0001)
                case _:
                    raise NotImplementedError("Currently only LBFGS and SGD, and Adam implemented with some default parameters")
        else:
            optimizer = optimizer([self.pos])

        for optim_step in range(num_optim):
            optimizer.zero_grad()
            energy = self.sys.calc_energy(self.pos, self.neighborlist)
            if print_details:
                print(optim_step, energy.item())
            if self.sys.use_neighborlist:
                self.neighborlist = self.neighbor_handling.check_recalc(self.pos)
            energy.backward()
            optimizer.step()

            if self.sys.periodic:
                self.pos = self.sys.apply_boundary_conditions(self.pos)
            self.pos = self.pos.detach().requires_grad_()


class MDSimulation(Simulation):
    """
    Class to perform Molecular Dynamics (MD) simulations.

    This class implements the Simulation interface for molecular dynamics simulations,
    handling integration of equations of motion, temperature control, and pressure control.

    Parameters
    ----------
    sys : object
        The system object containing information about the molecular system.
    integrator : object
        The integrator object used to perform the MD integration steps.
    initial_pos : torch.Tensor
        Tensor containing the initial positions of the atoms.
    initial_vel : torch.Tensor, optional
        Tensor containing the initial velocities of the atoms. If not provided, velocities 
        will be drawn from a Maxwell-Boltzmann distribution.
    temperature : float, optional
        Temperature for drawing initial velocities from a Maxwell-Boltzmann distribution. 
        Required if initial_vel is not provided.
    barostat : object, optional
        Barostat object for pressure control. Default is None.
    seed : int, optional
        Seed for the random number generator. Default is 42.
    neighbor_check_interval : int, optional
        Interval for checking and updating the neighbor list. Default is 1.

    Attributes
    ----------
    rng_generator : torch.Generator
        Random number generator for stochastic processes.
    sys : object
        The system object containing information about the molecular system.
    integrator : object
        The integrator object used to perform the MD integration steps.
    barostat : object or None
        Barostat object for pressure control.
    vel : torch.Tensor
        Tensor containing the current velocities of the atoms.
    pos : torch.Tensor
        Tensor containing the current positions of the atoms.
    temperature : float or None
        Temperature of the system.
    total_step_counter : int
        Counter for the total number of simulation steps performed.
    neighbor_check_interval : int
        Interval for checking and updating the neighbor list.
    neighbor_handling : NeighborHandling
        Object for handling neighbor lists.
    neighborlist : object or None
        Current neighbor list.

    Raises
    ------
    Exception
        If neither initial_vel nor temperature is provided.
        If a barostat is used without a thermostat.
    """

    def __init__(self, sys, integrator, initial_pos, initial_vel=None, temperature=None, barostat=None, seed=42, neighbor_check_interval=1):
        if initial_vel is None and temperature is None:
            raise Exception(
                "To initialize a simulation, one needs to set either initial velocities or drawn them from Maxwell-Boltzmann at a given temperature. Specify one.")

        self.rng_generator = torch.Generator(device=torch.get_default_device())
        self.rng_generator.manual_seed(seed)

        self.sys = sys
        self.integrator = integrator
        self.barostat = barostat
        if not hasattr(self.integrator, "temperature") and barostat is not None:
            raise Exception("Using a barostat is only possible with a thermostat that controls the temperature.")

        if initial_vel is None:
            initial_vel = torch.empty_like(initial_pos)
            initial_vel = maxwell_boltzmann_distr(self.sys, initial_vel, temperature, generator=self.rng_generator)

        self.vel = initial_vel.detach_().to(integrator.dtype)
        self.pos = initial_pos.detach_().requires_grad_().to(integrator.dtype)

        self.temperature = temperature

        self.total_step_counter = 0

        if self.sys.periodic:
            self.pos = self.sys.apply_boundary_conditions(self.pos)

        # init cell and neighborlist
        if self.sys.use_neighborlist:
            self.neighbor_check_interval = neighbor_check_interval
            self.neighbor_handling = NeighborHandling(self.sys.periodic, self.sys.num_atoms, self.sys.cutoff, self.sys.box, self.sys.all_exclusions)
            self.neighborlist = self.neighbor_handling.get_neighborlist(self.pos)
        else:
            self.neighborlist = None

        pos_prime = self.pos.clone()
        self.integrator.constraint_handler.apply_constraints(self.pos, pos_prime)
        self.pos = pos_prime
        self.integrator.constraint_handler.apply_velocity_constraints(self.pos, self.vel)

        self.integrator.update_acceleration(self.pos, self.neighborlist, self.sys)
        self.detach_()

        shift = self.integrator.shift
        if shift != 0.0:
            self.vel = self.vel - shift * self.integrator.a

    def step(self, num_steps=1, detach=True):
        """
        Perform a specified number of MD steps.

        This method advances the simulation by integrating the equations of motion,
        updating positions and velocities, applying constraints, and handling
        periodic boundary conditions.

        Parameters
        ----------
        num_steps : int, optional
            The number of MD steps to perform. Default is 1.
        detach : bool, optional
            Whether to detach tensors from the computation graph after each step.
            This saves memory but prevents backpropagation through multiple steps.
            Default is True.
            
        Raises
        ------
        AssertionError
            If NaN values are encountered in positions or velocities.
        """
        if self.sys.periodic:
            self.pos = self.sys.apply_boundary_conditions(self.pos)
        for step in range(num_steps):
            if self.sys.use_neighborlist and self.total_step_counter % self.neighbor_check_interval == 0:
                self.neighborlist = self.neighbor_handling.check_recalc(self.pos)

            self.pos, self.vel = self.integrator.step(self.pos, self.vel, self.neighborlist, self.sys, generator=self.rng_generator)

            if self.sys.periodic:
                self.pos = self.sys.apply_boundary_conditions(self.pos)

            if self.barostat is not None:
                if self.total_step_counter % self.barostat.frequency == 0:
                    self.pos = self.barostat.apply_barostat(self.pos, self, generator=self.rng_generator)

            if detach:
                self.detach_()
                self.neighbor_handling.original_pos.detach_()
                self.neighbor_handling.box.detach_()

            self.total_step_counter = self.total_step_counter + 1

            assert not torch.isnan(self.pos).any(), "Encountered NaN in positions. Something is wrong!"
            assert not torch.isnan(self.vel).any(), "Encountered NaN in velocities. Something is wrong!"


    def detach_(self):
        """
        Detach tensors from the computation graph.
        
        This method detaches the integrator, velocities, and positions from the
        computation graph to save memory, while ensuring positions remain
        differentiable for energy calculations.
        """
        self.integrator.detach_()
        self.vel.detach_()
        self.pos.detach_().requires_grad_()

    def update_box(self, box):
        """
        Update the simulation box dimensions.
        
        This method updates the box dimensions in the neighbor handling system,
        the integrator, and the system object.
        
        Parameters
        ----------
        box : torch.Tensor
            The new box dimensions.
        """
        self.neighbor_handling.update_box(box)
        self.integrator.update_box(box)
        self.sys.update_box(box)

    def measure_kinetic_energy(self):
        """
        Calculate the kinetic energy of the system.
        
        For velocity Verlet-like integrators with a non-zero shift parameter,
        this method applies the appropriate velocity correction before calculating
        the kinetic energy.
        
        Returns
        -------
        torch.Tensor
            The kinetic energy of the system.
        """
        shift = self.integrator.shift
        if shift != 0.0:
            self.integrator.update_acceleration(self.pos, self.neighborlist, self.sys)
            shifted_vel = self.vel + shift * self.integrator.a
            return kinetic_energy(shifted_vel, self.sys)
        else:
            return kinetic_energy(self.vel, self.sys)


class MCSimulation(Simulation):
    """
    A class to perform Monte Carlo (MC) simulations.

    This class implements the Simulation interface for Monte Carlo simulations,
    handling move selection, acceptance/rejection, and system configuration updates.

    Parameters
    ----------
    sys : object
        The system object containing information about the simulation system.
    initial_pos : torch.Tensor
        The initial positions of the particles in the system.
    move_set : list
        A list of possible moves to be performed during the simulation.
    move_prob : list
        A list of probabilities corresponding to each move in the move_set.
        Must sum to 1.0.
    temperature : float, optional
        The temperature of the system. Default is None.
    seed : int, optional
        The seed for the random number generator. Default is 42.
    neighbor_check_interval : int, optional
        The interval at which the neighbor list is checked and updated. Default is 10.

    Attributes
    ----------
    rng_generator : torch.Generator
        The random number generator for stochastic processes.
    sys : object
        The system object containing information about the simulation system.
    move_set : list
        A list of possible moves to be performed during the simulation.
    move_prob : torch.Tensor
        A tensor of probabilities corresponding to each move in the move_set.
    cum_move_prob : torch.Tensor
        A tensor of cumulative probabilities for the move_set, used for move selection.
    pos : torch.Tensor
        The current positions of the particles in the system.
    temperature : float or None
        The temperature of the system.
    total_step_counter : int
        The total number of steps performed in the simulation.
    neighbor_check_interval : int
        The interval at which the neighbor list is checked and updated.
    neighbor_handling : NeighborHandling or None
        The object responsible for handling neighbor lists.
    neighborlist : torch.Tensor or None
        The neighbor list for the system.
        
    Raises
    ------
    AssertionError
        If the lengths of move_set and move_prob don't match or if move_prob doesn't sum to 1.
    """

    def __init__(self, sys, initial_pos, move_set, move_prob, temperature=None, seed=42, neighbor_check_interval=10):
        self.rng_generator = torch.Generator(device=torch.get_default_device())
        self.rng_generator.manual_seed(seed)

        self.sys = sys

        self.move_set = move_set
        self.move_prob = torch.tensor(move_prob)
        self.cum_move_prob = self.move_prob.cumsum(dim=0)

        assert (len(move_set) == len(move_prob)), f"move set and probabilities shall match in length, retry"
        assert self.move_prob.sum().isclose(torch.tensor([1.0])), "Move probabilities need to sum to 1"

        self.pos = initial_pos.requires_grad_()
        self.temperature = temperature

        self.total_step_counter = 0

        if self.sys.periodic:
            self.pos = self.sys.apply_boundary_conditions(self.pos)

        if self.sys.use_neighborlist:
            self.neighbor_check_interval = neighbor_check_interval
            self.neighbor_handling = NeighborHandling(self.sys.periodic, self.sys.num_atoms, self.sys.cutoff, self.sys.box, self.sys.all_exclusions)
            self.neighborlist = self.neighbor_handling.get_neighborlist(self.pos)
        else:
            self.neighborlist = None
            self.neighbor_handling = None

    def step(self, num_steps=1, detach=True):
        """
        Perform a specified number of MC steps.

        Each step involves selecting a move according to the move probabilities,
        applying the move, and updating the system configuration.

        Parameters
        ----------
        num_steps : int, optional
            The number of MC steps to perform. Default is 1.
        detach : bool, optional
            Whether to detach tensors from the computation graph after each step.
            This saves memory but prevents backpropagation through multiple steps.
            Default is True.
        """
        for step in range(num_steps):
            selected_move_idx = torch.searchsorted(self.cum_move_prob, torch.empty(1).uniform_(generator=self.rng_generator))
            selected_move = self.move_set[selected_move_idx]
            self.pos, self.neighborlist = selected_move.move(self.pos, self.neighborlist, self.rng_generator, self.neighbor_handling)
            if self.sys.periodic:
                self.pos = self.sys.apply_boundary_conditions(self.pos)
            if detach:
                self.detach_()

    def update_box(self, box):
        """
        Update the simulation box dimensions.
        
        This method updates the box dimensions in the neighbor handling system,
        the system object, and all moves in the move set.
        
        Parameters
        ----------
        box : torch.Tensor
            The new box dimensions.
        """
        self.neighbor_handling.update_box(box)
        self.sys.update_box(box)
        for move in self.move_set:
            move.update_box(box)

    def detach_(self):
        """
        Detach tensors from the computation graph.
        
        This method detaches the integrators in moves (if present) and positions
        from the computation graph to save memory, while ensuring positions remain
        differentiable for energy calculations.
        """
        for move in self.move_set:
            if hasattr(move, "integrator"):
                move.integrator.detach_()
        self.pos.detach_()
        self.pos.requires_grad_()
