"""
Distribution module, currently mostly used for STHMC.

This module provides classes for handling various probability distributions used in
molecular simulations. It includes continuous, discretized, and truncated distributions,
with a particular focus on the folded normal distribution.

The module contains the following main classes:
- ContinuousDistribution: Base class for continuous probability distributions
- DiscretizedDistribution: Class for discretized versions of continuous distributions
- TruncatedDistribution: Class for truncated versions of discretized distributions 
- FoldedNormal: Implementation of the folded normal distribution

The distributions support computing log probabilities, CDFs, quantiles and other
statistical properties needed for molecular simulations.
"""

