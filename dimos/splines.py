class _BSplineFn(torch.autograd.Function):
    """
    A class to represent B-spline functions.

    Methods
    -------
    triangle(x)
        Calculate the triangle function.
    M(x, k)
        Calculate the B-spline function.
    forward(ctx, x, k)
        Forward pass for the B-spline function.
    backward(ctx, grad_output)
        Backward pass for the B-spline function.
    apply(cls, *args, **kwargs)
        Apply the B-spline function.
    """

    @staticmethod
    def triangle(x):
        """
        Calculate the triangle function.

        Parameters
        ----------
        x : torch.Tensor
            Input tensor.

        Returns
        -------
        torch.Tensor
            Triangle function values.
        """
        return (1 - abs(x - 1)) * (x >= 0) * (x <= 2)

    @staticmethod
    def M(x, k):
        """
        Calculate the B-spline function.

        Parameters
        ----------
        x : torch.Tensor
            Input tensor.
        k : int
            Order of the B-spline.

        Returns
        -------
        torch.Tensor
            B-spline function values.
        """
        if k <= 2:
            x = _BSplineFn.triangle(x)
            return x
        return x * _BSplineFn.M(x, k - 1) / (k - 1) + (k - x) * _BSplineFn.M(x - 1, k - 1) / (k - 1)

    @staticmethod
    def forward(ctx, x, k):
        """
        Forward pass for the B-spline function.

        Parameters
        ----------
        ctx : torch.autograd.Function
            Context object.
        x : torch.Tensor
            Input tensor.
        k : int
            Order of the B-spline.

        Returns
        -------
        torch.Tensor
            B-spline function values.
        """
        ctx.save_for_backward(x)
        ctx.k = k
        return _BSplineFn.M(x, k)

    @staticmethod
    def backward(ctx, grad_output):
        """
        Backward pass for the B-spline function.

        Parameters
        ----------
        ctx : torch.autograd.Function
            Context object.
        grad_output : torch.Tensor
            Gradient of the output.

        Returns
        -------
        torch.Tensor
            Gradient of the input.
        """
        x, = ctx.saved_tensors
        g = _BSplineFn.M(x, ctx.k - 1) - _BSplineFn.M(x - 1, ctx.k - 1)
        return g * grad_output, None

    @classmethod
    def apply(cls, *args, **kwargs):
        """
        Apply the B-spline function.

        Parameters
        ----------
        *args : tuple
            Arguments for the B-spline function.
        **kwargs : dict
            Keyword arguments for the B-spline function.

        Returns
        -------
        torch.Tensor
            B-spline function values.
        """
        return super(_BSplineFn, cls).apply(*args, **kwargs)


