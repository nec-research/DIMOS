"""
Barostat module.

This module provides Monte Carlo barostats for constant pressure molecular dynamics
simulations. It implements both isotropic and anisotropic pressure control based on
the Monte Carlo method described in:

The module contains the following main classes:
- MCBarostatIsotropic: Monte Carlo barostat for isotropic pressure control
- MCBarostatAnisotropic: Monte Carlo barostat for anisotropic pressure control

Note: MCBarostatAnisotropic is currently an experimental feature and should be used with caution.
"""

import torch
from dimos import constants
from warnings import warn

class MCBarostatIsotropic():
    """
    Monte Carlo barostat for isotropic pressure control.
    
    This class implements a Monte Carlo barostat that maintains constant pressure by
    attempting random volume changes and accepting/rejecting them based on the
    Metropolis criterion.

    Parameters
    ----------
    box : torch.Tensor
        Initial simulation box dimensions
    target_pressure : float, optional
        Target pressure in bar (default is 1.0)
    frequency : int, optional
        Number of steps between barostat moves (default is 25)
    scale : float, optional
        Initial scale for volume moves as fraction of total volume (default is 0.01)
    adaptive : bool, optional
        Whether to adaptively adjust move size (default is True)
    min_acceptance_rate : float, optional
        Minimum acceptable move acceptance rate (default is 0.25)
    max_acceptance_rate : float, optional
        Maximum acceptable move acceptance rate (default is 0.75)
    rescale_whole_system : bool, optional
        Whether to rescale all coordinates or just molecular centers (default is False)
    isotropic : bool, optional
        Whether to perform isotropic volume scaling (default is True)

    Attributes
    ----------
    frequency : int
        Steps between barostat moves
    target_pressure : float
        Target pressure in bar
    volume_scale : float
        Current scale for volume moves
    adaptive : bool
        Whether move size is adaptively adjusted
    min_acceptance_rate : float
        Minimum acceptance rate threshold
    max_acceptance_rate : float 
        Maximum acceptance rate threshold
    num_moves_attempted : int
        Number of attempted moves
    num_moves_accepted : int
        Number of accepted moves
    rescale_whole_system : bool
        Whether all coordinates are rescaled
    isotropic : bool
        Whether scaling is isotropic
    """

    def __init__(self, box, target_pressure=1.0, frequency=25, scale=0.01, adaptive=True, min_acceptance_rate=0.25, max_acceptance_rate=0.75, rescale_whole_system=False, isotropic=True):
        self.frequency = frequency
        self.target_pressure = target_pressure
        self.volume_scale = scale * torch.prod(box) # initial scale of volume change is 1%

        self.adaptive = adaptive
        self.min_acceptance_rate = min_acceptance_rate
        self.max_acceptance_rate = max_acceptance_rate
        self.num_moves_attempted = 0
        self.num_moves_accepted = 0

        self.rescale_whole_system = rescale_whole_system
        self.isotropic = isotropic

    def mean_position(self, positions, box):
        """
        Calculate mean position of atoms/molecules accounting for periodic boundaries.

        Parameters
        ----------
        positions : torch.Tensor
            Atomic/molecular positions
        box : torch.Tensor
            Box dimensions

        Returns
        -------
        torch.Tensor
            Mean positions within periodic box
        """
        position_angles = 2.0 * torch.pi * positions / box.unsqueeze(0).unsqueeze(0)
        cosine_avg = torch.mean(torch.cos(position_angles), dim=1)
        sine_avg = torch.mean(torch.sin(position_angles), dim=1)
        theta_avg = torch.arctan2(sine_avg, cosine_avg)
        return (theta_avg * box / (2.0 * torch.pi)) % box

    def apply_barostat(self, pos, simulation, generator=None):
        """
        Apply the Monte Carlo barostat move.

        Parameters
        ----------
        pos : torch.Tensor
            Current atomic positions
        simulation : object
            Simulation object containing system information
        generator : torch.Generator, optional
            Random number generator

        Returns
        -------
        torch.Tensor
            New atomic positions after barostat move
        """
        initial_pos = pos.clone()
        initial_box = simulation.sys.box.clone()

        initial_volume = torch.prod(initial_box)

        # This is questionable in my opinion, see https://ucla-biostats-285.github.io/reading/AdaptiveMCMC.pdf
        if self.adaptive:
            if self.num_moves_attempted >= 10:
                if self.num_moves_accepted < self.min_acceptance_rate * self.num_moves_attempted:
                    self.volume_scale = self.volume_scale / 1.1
                    self.num_moves_attempted = 0
                    self.num_moves_accepted = 0
                elif self.num_moves_accepted > self.max_acceptance_rate * self.num_moves_attempted:
                    self.volume_scale = min(self.volume_scale * 1.1, initial_volume * 0.3)
                    self.num_moves_attempted = 0
                    self.num_moves_accepted = 0

        self.num_moves_attempted = self.num_moves_attempted + 1

        if simulation.sys.use_neighborlist:
            initial_energy = simulation.sys.calc_energy(initial_pos, simulation.neighborlist)
        else:
            initial_energy = simulation.sys.calc_energy(initial_pos)

        delta_volume = self.volume_scale * 2.0 * (torch.empty([1]).uniform_(generator=generator) - 0.5)
        new_volume = initial_volume + delta_volume
        length_scale = (new_volume / initial_volume)**(1.0 / 3.0)

        new_box = initial_box * length_scale
        simulation.update_box(new_box)

        if self.rescale_whole_system:
            pos = initial_pos * length_scale
            degree_of_freedom = pos.size(0)
        else:
            # First all atoms with length three. This includes the large class of water and allows parallelizing over it.
            water_positions = pos[simulation.sys.length3_molecules]
            water_centers = self.mean_position(water_positions, new_box).unsqueeze(1)

            new_water_centers = water_centers * length_scale
            offset = new_water_centers - water_centers

            pos[simulation.sys.length3_molecules] = water_positions + offset

            # Now all other molecules which have a different length, over which it is not trivial to batch.
            for mol in simulation.sys.other_molecules:
                molecule_positions = pos[mol]
                molecule_center = self.mean_position(molecule_positions.unsqueeze(0), new_box).squeeze(0)
                
                new_molecule_center = molecule_center * length_scale
                offset = new_molecule_center - molecule_center

                pos[mol] = molecule_positions + offset
            degree_of_freedom = simulation.sys.num_molecules

        pos = simulation.sys.apply_boundary_conditions(pos)

        if simulation.sys.use_neighborlist:
            original_neighborlist = simulation.neighborlist.clone()
            original_pos = simulation.neighbor_handling.original_pos.clone()
            simulation.neighborlist = simulation.neighbor_handling.get_neighborlist(pos)
        
        new_energy, _, new_acceleration = simulation.sys.calc_energy(pos, simulation.neighborlist, return_forces=True)

        energy_change = new_energy - initial_energy
        pressure_volume_work = self.target_pressure * delta_volume * constants.BAR_TO_INTERNAL_PRESSURE
        jacobian = -degree_of_freedom * torch.log(new_volume / initial_volume) * constants.BOLTZMANN * simulation.temperature

        acceptance_probability = torch.exp(-(energy_change + pressure_volume_work + jacobian) / (constants.BOLTZMANN * simulation.temperature))

        if acceptance_probability >= torch.empty((1)).uniform_(generator=generator):
            # Accept move
            if hasattr(simulation.integrator, "a"):
                simulation.integrator.a = new_acceleration.to(simulation.integrator.dtype)
            self.num_moves_accepted = self.num_moves_accepted + 1
            return pos
        else:
            # Reject move
            simulation.update_box(initial_box)
            simulation.neighbor_handling.original_pos = original_pos
            simulation.neighbor_handling.neighborlist = original_neighborlist
            simulation.neighborlist = original_neighborlist
            return initial_pos

class MCBarostatAnisotropic():
    """
    Monte Carlo barostat for anisotropic pressure control.
    
    This class implements a Monte Carlo barostat that maintains constant pressure by
    attempting random volume changes along individual axes and accepting/rejecting them
    based on the Metropolis criterion.

    Note: MCBarostatAnisotropic is currently an experimental feature and should be used with caution.

    Parameters
    ----------
    box : torch.Tensor
        Initial simulation box dimensions
    scale_axis : tuple of bool, optional
        Which axes to scale (default is (True, True, True))
    target_pressure : tuple of float or float, optional
        Target pressure(s) in bar for each axis (default is (1.0, 1.0, 1.0))
    frequency : int, optional
        Number of steps between barostat moves (default is 25)
    scale : float, optional
        Initial scale for volume moves as fraction of total volume (default is 0.01)
    adaptive : bool, optional
        Whether to adaptively adjust move size (default is True)
    min_acceptance_rate : float, optional
        Minimum acceptable move acceptance rate (default is 0.25)
    max_acceptance_rate : float, optional
        Maximum acceptable move acceptance rate (default is 0.75)
    rescale_whole_system : bool, optional
        Whether to rescale all coordinates or just molecular centers (default is False)
    isotropic : bool, optional
        Whether to perform isotropic volume scaling (default is False)

    Attributes
    ----------
    frequency : int
        Steps between barostat moves
    target_pressure : torch.Tensor
        Target pressures in bar for each axis
    scale_axis : torch.Tensor
        Boolean mask for which axes to scale
    volume_scale : torch.Tensor
        Current scale for volume moves along each axis
    adaptive : bool
        Whether move size is adaptively adjusted
    min_acceptance_rate : float
        Minimum acceptance rate threshold
    max_acceptance_rate : float
        Maximum acceptance rate threshold
    num_moves_attempted : torch.Tensor
        Number of attempted moves per axis
    num_moves_accepted : torch.Tensor
        Number of accepted moves per axis
    rescale_whole_system : bool
        Whether all coordinates are rescaled
    isotropic : bool
        Whether scaling is isotropic
    """

    def __init__(self, box, scale_axis=(True, True, True), target_pressure=(1.0, 1.0, 1.0), frequency=25, scale=0.01, adaptive=True, min_acceptance_rate=0.25, max_acceptance_rate=0.75, rescale_whole_system=False, isotropic=False):
        warn("The anisotropic barostat is currently an experimental feature. While it should work mostly correct, it has not been thoroughly tested. Please be careful when using it and compare to established implementations.", stacklevel=2)
        
        self.frequency = frequency
        if isinstance(target_pressure, float):
            warn("Only one target pressure defined in anisoropic barostat. Using the same value for all axis")
            self.target_pressure = torch.ones(3)*target_pressure
        else:
            assert len(target_pressure)==3, "Need to specify pressure in all directions"
            self.target_pressure = torch.tensor(target_pressure)
        self.scale_axis = torch.tensor(scale_axis)
        assert self.scale_axis.sum()>0, "Need to specify at least one axis that should be updated by barostat"
        
        self.volume_scale = scale * torch.prod(box) * torch.ones(3) # initial scale of volume change is 1%

        self.adaptive = adaptive
        self.min_acceptance_rate = min_acceptance_rate
        self.max_acceptance_rate = max_acceptance_rate
        self.num_moves_attempted = torch.zeros(3)
        self.num_moves_accepted = torch.zeros(3)

        self.rescale_whole_system = rescale_whole_system
        self.isotropic = isotropic

    def mean_position(self, positions, box):
        """
        Calculate mean position of atoms/molecules accounting for periodic boundaries.

        Parameters
        ----------
        positions : torch.Tensor
            Atomic/molecular positions
        box : torch.Tensor
            Box dimensions

        Returns
        -------
        torch.Tensor
            Mean positions within periodic box
        """
        position_angles = 2.0 * torch.pi * positions / box.unsqueeze(0).unsqueeze(0)
        cosine_avg = torch.mean(torch.cos(position_angles), dim=1)
        sine_avg = torch.mean(torch.sin(position_angles), dim=1)
        theta_avg = torch.arctan2(sine_avg, cosine_avg)
        return (theta_avg * box / (2.0 * torch.pi)) % box

    def apply_barostat(self, pos, simulation, generator=None):
        """
        Apply the Monte Carlo barostat move.

        Parameters
        ----------
        pos : torch.Tensor
            Current atomic positions
        simulation : object
            Simulation object containing system information
        generator : torch.Generator, optional
            Random number generator

        Returns
        -------
        torch.Tensor
            New atomic positions after barostat move
        """
        initial_pos = pos.clone()
        initial_box = simulation.sys.box.clone()

        initial_volume = torch.prod(initial_box)
        while True:
            axis = torch.randint(3, (1,), generator=generator)[0]
            if self.scale_axis[axis]:
                break

        # This is questionable in my opinion, see https://ucla-biostats-285.github.io/reading/AdaptiveMCMC.pdf
        if self.adaptive:
            if self.num_moves_attempted[axis] >= 10:
                if self.num_moves_accepted[axis] < self.min_acceptance_rate * self.num_moves_attempted[axis]:
                    self.volume_scale[axis] = self.volume_scale[axis] / 1.1
                    self.num_moves_attempted[axis] = 0
                    self.num_moves_accepted[axis] = 0
                elif self.num_moves_accepted[axis] > self.max_acceptance_rate * self.num_moves_attempted[axis]:
                    self.volume_scale[axis] = min(self.volume_scale[axis] * 1.1, initial_volume * 0.3)
                    self.num_moves_attempted[axis] = 0
                    self.num_moves_accepted[axis] = 0

        self.num_moves_attempted = self.num_moves_attempted + 1

        if simulation.sys.use_neighborlist:
            initial_energy = simulation.sys.calc_energy(initial_pos, simulation.neighborlist)
        else:
            initial_energy = simulation.sys.calc_energy(initial_pos)

        delta_volume = self.volume_scale[axis] * 2.0 * (torch.empty([1]).uniform_(generator=generator) - 0.5)
        new_volume = initial_volume + delta_volume
        
        length_scale = torch.ones(3)
        length_scale[axis] = new_volume / initial_volume

        new_box = initial_box * length_scale
        simulation.update_box(new_box)

        if self.rescale_whole_system:
            pos = initial_pos * length_scale
            degree_of_freedom = pos.size(0)
        else:
            # First all atoms with length three. This includes the large class of water and allows parallelizing over it.
            water_positions = pos[simulation.sys.length3_molecules]
            water_centers = self.mean_position(water_positions, new_box).unsqueeze(1)

            new_water_centers = water_centers * length_scale
            offset = new_water_centers - water_centers

            pos[simulation.sys.length3_molecules] = water_positions + offset

            # Now all other molecules which have a different length, over which it is not trivial to batch.
            for mol in simulation.sys.other_molecules:
                molecule_positions = pos[mol]
                molecule_center = self.mean_position(molecule_positions.unsqueeze(0), new_box).squeeze(0)
                
                new_molecule_center = molecule_center * length_scale
                offset = new_molecule_center - molecule_center

                pos[mol] = molecule_positions + offset
            degree_of_freedom = simulation.sys.num_molecules

        pos = simulation.sys.apply_boundary_conditions(pos)

        if simulation.sys.use_neighborlist:
            original_neighborlist = simulation.neighborlist.clone()
            original_pos = simulation.neighbor_handling.original_pos.clone()
            simulation.neighborlist = simulation.neighbor_handling.get_neighborlist(pos)
        
        new_energy, _, new_acceleration = simulation.sys.calc_energy(pos, simulation.neighborlist, return_forces=True)

        energy_change = new_energy - initial_energy
        pressure_volume_work = self.target_pressure[axis] * delta_volume * constants.BAR_TO_INTERNAL_PRESSURE
        jacobian = -degree_of_freedom * torch.log(new_volume / initial_volume) * constants.BOLTZMANN * simulation.temperature

        acceptance_probability = torch.exp(-(energy_change + pressure_volume_work + jacobian) / (constants.BOLTZMANN * simulation.temperature))

        if acceptance_probability >= torch.empty((1)).uniform_(generator=generator):
            # Accept move
            if hasattr(simulation.integrator, "a"):
                simulation.integrator.a = new_acceleration.to(simulation.integrator.dtype)
            self.num_moves_accepted = self.num_moves_accepted + 1
            return pos
        else:
            # Reject move
            simulation.update_box(initial_box)
            simulation.neighbor_handling.original_pos = original_pos
            simulation.neighbor_handling.neighborlist = original_neighborlist
            simulation.neighborlist = original_neighborlist
            return initial_pos
