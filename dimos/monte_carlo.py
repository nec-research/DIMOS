"""
Monte Carlo simulations module.

This module provides implementations of various Monte Carlo (MC) sampling methods
for molecular simulations. It includes standard and advanced MC moves
like Hamiltonian Monte Carlo (HMC), Self-Tuning HMC (STHMC), and 
Metropolis-Adjusted Langevin Algorithm (MALA).

The module contains the following main classes:

- MCMove: Base class defining the interface for Monte Carlo moves
- HMC: Implementation of Hamiltonian Monte Carlo
- STHMC: Implementation of Self-Tuning HMC
- MALA: Implementation of Metropolis-Adjusted Langevin Algorithm

These classes provide functionality for efficient sampling of configuration space
in molecular systems while maintaining proper statistical mechanics.
"""
import dimos

import torch

from dimos.utils import maxwell_boltzmann_distr, kinetic_energy
from dimos.integrators import VelocityVerlet
from dimos import constants


from warnings import warn

class VelocityVerletHMC(VelocityVerlet):
    
    """
    Velocity Verlet integrator for Hamiltonian Monte Carlo.
    
    This class extends the standard VelocityVerlet integrator with functionality
    specific to Hamiltonian Monte Carlo simulations, including tracking of energy,
    forces, and acceleration for acceptance/rejection decisions.

    Parameters
    ----------
    timestep : float or torch.Tensor
        The timestep for the integrator.
    sys : object
        The system object containing information about the molecular system.
        
    Attributes
    ----------
    timestep : torch.Tensor
        The timestep for the integrator.
    dtype : torch.dtype
        The data type for the integrator.
    e : torch.Tensor
        The energy of the system.
    f : torch.Tensor
        The forces acting on the particles.
    a : torch.Tensor
        The accelerations of the particles.
    """

    def update_acceleration(self, pos, neighborlist, sys):
        """
        Update the acceleration based on forces from the system.

        This method calculates the energy and forces, then derives acceleration
        from forces for use in the integration step.

        Parameters
        ----------
        pos : torch.Tensor
            The positions of the particles.
        neighborlist : torch.Tensor or None
            The neighbor list for efficient force calculation.
        sys : object
            The system object containing force field information.
        """
        self.e, self.f, self.a = sys.calc_energy(pos, neighborlist, return_forces=True)
        self.a.to(self.dtype)

    def get_variables_to_detach(self):
        """
        Get the variables that should be detached from the computation graph.
        
        Returns
        -------
        list
            A list of tensors to detach from the computation graph.
        """
        return [self.a, self.f, self.e]

class MCMove():
    """
    Base class for Monte Carlo moves.
    
    This abstract class defines the interface for all Monte Carlo moves used in
    MCSimulation. Subclasses must implement the move method to define specific
    Monte Carlo move types.
    
    Methods
    -------
    update_box(box)
        Update the simulation box dimensions.
    move(pos, neighborlist, generator, neighbor_handling)
        Perform the Monte Carlo move.
    """
    
    def update_box(self, box):
        """
        Update the simulation box dimensions.
        
        This method is called when the simulation box changes, for example
        during NPT simulations. The default implementation does nothing.
        
        Parameters
        ----------
        box : torch.Tensor
            The new box dimensions.
        """
        pass
    
    def move(self, pos, neighborlist, generator=None, neighbor_handling=None):
        """
        Perform a Monte Carlo move.
        
        This method must be implemented by all subclasses to define the specific
        Monte Carlo move behavior.
        
        Parameters
        ----------
        pos : torch.Tensor
            The current positions of the particles.
        neighborlist : object or None
            The neighbor list for the system.
        generator : torch.Generator, optional
            Random number generator for reproducibility. Default is None.
        neighbor_handling : object, optional
            The neighbor handling object for updating the neighbor list. Default is None.
            
        Raises
        ------
        NotImplementedError
            If the subclass does not implement this method.
        """
        raise NotImplementedError("Move must be implemented by all MC moves")



class HMC(MCMove):
    """
    Hamiltonian Monte Carlo (HMC) class for performing HMC moves in a molecular simulation.
    
    HMC combines molecular dynamics with Monte Carlo to efficiently sample configuration space.
    It uses short MD trajectories as proposals for MC moves, accepting or rejecting based on
    the change in total energy.

    Parameters
    ----------
    temperature : float
        The temperature of the system.
    timestep : float
        The time step for the integrator.
    num_steps : int
        The number of integration steps per HMC move.
    sys : object
        The system object containing information about the molecular system.
    jitter_timestep : float, optional
        Amount of random variation to apply to the timestep. Default is 0.1.
    neighbor_check_interval : int, optional
        The interval at which to check and update the neighbor list. Default is 1.
    detach : bool, optional
        Whether to detach tensors from the computation graph during integration. Default is True.

    Attributes
    ----------
    temperature : float
        The temperature of the system.
    timestep : torch.Tensor
        The time step for the integrator.
    jitter_timestep : torch.Tensor
        Amount of random variation applied to the timestep.
    num_steps : int
        The number of integration steps per HMC move.
    sys : object
        The system object containing information about the molecular system.
    kBT : float
        The product of the Boltzmann constant and the temperature.
    integrator : VelocityVerlet
        The integrator used for the simulation.
    neighbor_check_interval : int
        The interval at which to check and update the neighbor list.
    detach : bool
        Whether to detach tensors from the computation graph during integration.
    accepted_moves : int
        Counter for the number of accepted moves.
    total_moves : int
        Counter for the total number of attempted moves.
    """

    def __init__(self, temperature, timestep, num_steps, sys, jitter_timestep=0.1, neighbor_check_interval=1, detach=True):
        """
        Initializes the HMC class with the given parameters.

        Parameters
        ----------
        temperature : float
            The temperature of the system.
        timestep : float
            The time step for the integrator.
        num_steps : int
            The number of integration steps per HMC move.
        sys : object
            The system object containing information about the molecular system.
        jitter_timestep : float, optional
            Amount of random variation to apply to the timestep. Default is 0.1.
        neighbor_check_interval : int, optional
            The interval at which to check and update the neighbor list. Default is 1.
        detach : bool, optional
            Whether to detach tensors from the computation graph during integration. Default is True.
        """
        self.temperature = temperature
        self.timestep = torch.tensor([timestep])
        self.jitter_timestep = torch.tensor([jitter_timestep])
        self.num_steps = num_steps
        self.sys = sys
        self.kBT = self.temperature * constants.BOLTZMANN
        self.integrator = VelocityVerlet(self.timestep, self.sys)
        self.neighbor_check_interval = neighbor_check_interval
        self.detach = detach

        self.accepted_moves = 0
        self.total_moves = 0

    def move(self, pos, neighborlist, generator=None, neighbor_handling=None):
        """
        Perform an HMC move.
        
        This method implements the Hamiltonian Monte Carlo algorithm:
        1. Draw initial velocities from Maxwell-Boltzmann distribution
        2. Compute initial energy
        3. Perform MD integration for a fixed number of steps
        4. Compute final energy
        5. Accept or reject the move based on the energy difference

        Parameters
        ----------
        pos : torch.Tensor
            The positions of the atoms.
        neighborlist : object or None
            The neighbor list for the system.
        generator : torch.Generator, optional
            A random number generator for reproducibility. Default is None.
        neighbor_handling : object, optional
            The neighbor handling object for updating the neighbor list. Default is None.

        Returns
        -------
        pos : torch.Tensor
            The updated positions of the atoms (or original positions if move rejected).
        neighborlist : object
            The updated neighbor list.
            
        Notes
        -----
        This method tracks acceptance statistics and applies a small random jitter
        to the timestep to improve sampling.
        """
        if self.sys.use_neighborlist:
            neighborlist = neighbor_handling.check_recalc(pos)

        initial_pos = pos
        vel = maxwell_boltzmann_distr(self.sys, torch.empty_like(pos), self.temperature, generator=generator)
        self.initial_potential_energy, initial_f, initial_a = self.sys.calc_energy(pos, neighborlist, return_forces=True)
        self.initial_kinetic_energy = kinetic_energy(vel, self.sys)
        self.initial_total_energy = self.initial_potential_energy + self.initial_kinetic_energy
        self.integrator.a = initial_a

        self.integrator.set_timestep(self.timestep + torch.sqrt(self.jitter_timestep * self.timestep) * torch.empty(1).normal_(generator=generator))

        for step in range(self.num_steps):
            if self.sys.use_neighborlist and step % self.neighbor_check_interval == 0:
                neighborlist = neighbor_handling.check_recalc(pos)
            
            pos, vel = self.integrator.step(pos, vel, neighborlist, self.sys, generator=generator)
            
            if self.sys.periodic:
                self.pos = self.sys.apply_boundary_conditions(self.pos)
            
            if self.detach:
                self.integrator.detach_()
                vel.detach_()
                pos.detach_()
                pos.requires_grad_()


        self.final_potential_energy = self.sys.calc_energy(pos, neighborlist, return_forces=False)
        self.final_kinetic_energy = kinetic_energy(vel, self.sys)
        self.final_total_energy = self.final_potential_energy + self.final_kinetic_energy
        self.delta_energy = self.final_total_energy - self.initial_total_energy
        self.acceptance_prob = torch.exp(-self.delta_energy / self.kBT)

        acceptance_decision = self.acceptance_prob > torch.empty_like(self.acceptance_prob).uniform_(generator=generator)

        if not acceptance_decision:
            pos = initial_pos

        self.accepted_moves = self.accepted_moves + acceptance_decision
        self.total_moves = self.total_moves + 1

        return pos, neighborlist


class STHMC(HMC):
    """
    Stochastic Truncated Hamiltonian Monte Carlo (STHMC) class.
    
    STHMC extends HMC by using a truncated distribution to determine the number of
    integration steps and applying a loss function during the trajectory. This allows
    for more efficient sampling and can be used for biased simulations.
    
    Parameters
    ----------
    temperature : float
        The temperature of the system.
    timestep : float
        The time step for the integrator.
    truncated_distribution : object
        Object that determines the truncation of the integration trajectory.
    loss_fn : callable
        Function to compute a loss during the trajectory.
    sys 
        The system object containing information about the molecular system.
    jitter_timestep : float, optional
        Amount of random variation to apply to the timestep. Default is 0.01.
    neighbor_check_interval : int, optional
        The interval at which to check and update the neighbor list. Default is 1.
    detach : bool, optional
        Whether to detach tensors from the computation graph during integration. Default is True.
        
    Attributes
    ----------
    truncated_distribution : object
        Object that determines the truncation of the integration trajectory.
    loss_fn : callable
        Function to compute a loss during the trajectory.
    integrator : VelocityVerletHMC
        The integrator used for the simulation.
    acceptance_prob : torch.Tensor or None
        The probability of accepting the most recent move.
    loss : float
        The accumulated loss during the trajectory.
    """

    def __init__(
            self,
            temperature,
            timestep,
            truncated_distribution,
            loss_fn,
            sys,
            jitter_timestep=0.01,
            neighbor_check_interval=1,
            detach=True):
        super().__init__(temperature, timestep, truncated_distribution.compute_truncation(), sys,
                         jitter_timestep=jitter_timestep, neighbor_check_interval=neighbor_check_interval, detach=detach)
        self.truncated_distribution = truncated_distribution
        self.loss_fn = loss_fn
        self.integrator = VelocityVerletHMC(self.timestep, self.sys)
        self.acceptance_prob = None

    def move(self, pos, neighborlist, generator=None, neighbor_handling=None):
        """
        Perform an STHMC move.
        
        This method extends the HMC move by using a truncated distribution to determine
        the number of integration steps and applying a loss function during the trajectory.
        It also selects a specific configuration from the trajectory for acceptance/rejection.

        Parameters
        ----------
        pos : torch.Tensor
            The positions of the atoms.
        neighborlist : object or None
            The neighbor list for the system.
        generator : torch.Generator, optional
            A random number generator for reproducibility. Default is None.
        neighbor_handling : object, optional
            The neighbor handling object for updating the neighbor list. Default is None.

        Returns
        -------
        pos : torch.Tensor
            The updated positions of the atoms (or original positions if move rejected).
        neighborlist : object
            The updated neighbor list.
            
        Notes
        -----
        This method handles NaN values in positions or acceptance probability by
        rejecting the move. It also accumulates a loss during the trajectory that
        can be used for training or biasing.
        """
        if self.sys.use_neighborlist:
            neighborlist = neighbor_handling.check_recalc(pos)

        self.initial_pos = pos
        vel = maxwell_boltzmann_distr(self.sys, torch.empty_like(pos), self.temperature, generator=generator)
        self.initial_potential_energy, initial_f, initial_a = self.sys.calc_energy(pos, neighborlist, return_forces=True)
        self.initial_kinetic_energy = kinetic_energy(vel, self.sys)
        self.initial_total_energy = self.initial_potential_energy + self.initial_kinetic_energy
        self.integrator.a = initial_a

        local_timestep = torch.abs(self.timestep + torch.sqrt(self.jitter_timestep * self.timestep) *
                                   torch.empty(1).normal_(generator=generator))  # Pick from folded normal
        self.integrator.set_timestep(local_timestep)

        self.num_steps, self.weights = self.truncated_distribution.compute_truncation_and_weights()
        selected_index = torch.multinomial(self.weights, num_samples=1).item()

        self.loss = 0
        for step in range(self.num_steps):
            if self.sys.use_neighborlist and step % self.neighbor_check_interval == 0:
                neighborlist = neighbor_handling.check_recalc(pos)
            
            pos, vel = self.integrator.step(pos, vel, neighborlist, self.sys, generator=generator)
            
            if self.sys.periodic:
                pos = self.sys.apply_boundary_conditions(pos)

            if step == selected_index:
                pos_for_acceptance = pos.clone()
                vel_for_acceptance = vel.clone()
                neighborlist_for_acceptance = neighborlist.clone()
            
            self.loss = self.loss + self.loss_fn(self, step, pos, vel)
            if self.detach:
                self.integrator.detach_()
                vel.detach_()
                pos.detach_()
                pos.requires_grad_()

        pos = pos_for_acceptance
        vel = vel_for_acceptance
        neighborlist = neighborlist_for_acceptance

        self.final_potential_energy = self.sys.calc_energy(pos, neighborlist, return_forces=False)
        self.final_kinetic_energy = kinetic_energy(vel, self.sys)
        self.final_total_energy = self.final_potential_energy + self.final_kinetic_energy
        self.delta_energy = self.final_total_energy - self.initial_total_energy
        self.acceptance_prob = torch.exp(-self.delta_energy / self.kBT)

        acceptance_decision = self.acceptance_prob > torch.empty_like(self.acceptance_prob).uniform_(generator=generator)

        if not acceptance_decision or torch.isnan(pos).any() or torch.isnan(self.acceptance_prob).any():
            pos = self.initial_pos
            acceptance_decision = 0

        self.accepted_moves = self.accepted_moves + acceptance_decision
        self.total_moves = self.total_moves + 1

        return pos, neighborlist


class MALA(MCMove):
    """
    Metropolis-Adjusted Langevin Algorithm (MALA) class for performing MALA moves.
    
    MALA combines Langevin dynamics with Metropolis-Hastings acceptance/rejection
    to efficiently sample configuration space while maintaining the correct equilibrium
    distribution. It uses gradient information to guide the proposals.

    Parameters
    ----------
    temperature : float
        The temperature of the system.
    timestep : float
        The timestep for the MALA move.
        
    Attributes
    ----------
    temperature : float
        The temperature of the system.
    timestep : float
        The timestep for the MALA move.
    sqrt_two_dt : torch.Tensor
        The square root of two times the timestep, used in the stochastic term.
        
    Notes
    -----
    This is an experimental feature and may not be fully tested.
    """

    def __init__(self, temperature, timestep):
        """
        Initializes the MALA class with the given temperature and timestep.

        Parameters
        ----------
        temperature : float
            The temperature of the system.
        timestep : float
            The timestep for the MALA move.
        """
        warn("MALA is not well tested and therefore is currently an experimental feature!")
        self.temperature = temperature
        self.timestep = timestep
        self.sqrt_two_dt = torch.sqrt(torch.tensor([2 * self.timestep]))

    def vlog_Q(self, forces, z_prime, z):
        """
        Calculate the logarithm of the proposal distribution Q.
        
        This method computes the log probability of proposing configuration z_prime
        given the current configuration z and forces.

        Parameters
        ----------
        forces : torch.Tensor
            The forces acting on the atoms.
        z_prime : torch.Tensor
            The proposed positions of the atoms.
        z : torch.Tensor
            The current positions of the atoms.

        Returns
        -------
        torch.Tensor
            The logarithm of the proposal distribution Q.
        """
        return -(torch.linalg.vector_norm(z_prime - z + self.timestep * forces, p=2, dim=1) ** 2) / (4 * self.timestep)

    def move(self, sys, pos, neighborlist, generator=None, neighbor_handling=None):
        """
        Perform a MALA move.
        
        This method implements the Metropolis-Adjusted Langevin Algorithm:
        1. Compute forces at current position
        2. Propose new position using Langevin dynamics
        3. Compute the acceptance ratio considering both forward and reverse proposals
        4. Accept or reject the move based on the acceptance ratio

        Parameters
        ----------
        sys : object
            The system object containing information about the molecular system.
        pos : torch.Tensor
            The positions of the atoms.
        neighborlist : object or None
            The neighbor list for the system.
        generator : torch.Generator, optional
            A random number generator for reproducibility. Default is None.
        neighbor_handling : object, optional
            The neighbor handling object for updating the neighbor list. Default is None.

        Returns
        -------
        pos : torch.Tensor
            The updated positions of the atoms (or original positions if move rejected).
        neighborlist : object
            The updated neighbor list.
            
        Notes
        -----
        The method includes a detailed balance correction to ensure the correct
        equilibrium distribution is sampled.
        """
        # TODO: Neighborlist checks may be overdone here...
        if sys.use_neighborlist:
            neighborlist = neighbor_handling.check_recalc(pos)

        energy, forces, acceleration = sys.calc_energy(pos, return_forces=True)
        # I switched to acceleration = force/mass. I think this is correct...
        prop_pos = pos - self.timestep * acceleration + self.sqrt_two_dt * torch.empty_like(acceleration).normal_(generator=generator)
        # TODO: Should this not be a different sign for calc_energy and energy? mean appears to mess some stuff up.
        log_ratio = self.vlog_Q(forces, pos, prop_pos) - self.vlog_Q(forces, prop_pos, pos) - sys.calc_energy(prop_pos).mean() + energy.mean()
        alpha = torch.exp(log_ratio) - torch.empty_like(log_ratio).uniform_(generator=generator)
        pos = torch.where(alpha > 0, prop_pos, pos)

        if sys.use_neighborlist:
            neighborlist = neighbor_handling.check_recalc(pos)

        return pos, neighborlist
