"""
Ewald summation methods for electrostatic interactions.

This module provides implementations of Ewald summation techniques for calculating
long-range electrostatic interactions in molecular systems, including standard
Ewald summation and Particle Mesh Ewald (PME). These methods efficiently handle
the calculation of electrostatic energies in periodic systems.
"""

import torch
import math

from dimos.utils import get_distances_edge_list
from dimos.splines import bspline
from dimos import constants


class CommonNonBonded():
   """
   Base class for non-bonded interaction calculations.
   
   This class provides common functionality for handling non-bonded interactions
   such as electrostatics and van der Waals forces.
   
   Methods
   -------
   update_box(box)
       Update the simulation box dimensions.
   """
   
   def update_box(self, box):
       """
       Update the simulation box dimensions.
       
       Parameters
       ----------
       box : torch.Tensor
           The new box dimensions.
       """
       self.box = box


class CommonEwald(CommonNonBonded):
    """
    Base class for Ewald summation methods.
    
    This abstract class provides common functionality for different Ewald summation
    implementations, including direct space calculations, self-energy corrections,
    and handling of exclusions. Subclasses must implement the reciprocal space
    calculation.

    Parameters
    ----------
    charges : torch.Tensor
        Charges of the atoms.
    tolerance : float
        Tolerance for the Ewald summation, controlling accuracy.
    scee : torch.Tensor
        Scaling factor for 1-4 electrostatic interactions.
    all_exclusions_without_improper : list or torch.Tensor
        List of all exclusions without improper torsions.
    dihedral_exclusions : list or torch.Tensor
        List of dihedral (1-4) exclusions.
    periodic : bool
        Whether to use periodic boundary conditions.
    box : torch.Tensor
        Dimensions of the simulation box.

    Attributes
    ----------
    charges : torch.Tensor
        Charges of the atoms.
    scee : torch.Tensor
        Scaling factor for 1-4 interactions.
    periodic : bool
        Whether periodic boundary conditions are used.
    box : torch.Tensor
        Dimensions of the simulation box.
    tolerance : float
        Tolerance for the Ewald summation.
    all_exclusions : list or torch.Tensor
        List of all exclusions without improper torsions.
    dihedral_exclusions : list or torch.Tensor
        List of dihedral exclusions.
    one_four_charges : torch.Tensor
        Charges for 1-4 interactions, scaled by scee.
    sqrt_pi : float
        Square root of pi.
    alpha : float or None
        Ewald parameter, controlling the split between direct and reciprocal space.
    """

    def __init__(
            self,
            charges: torch.Tensor,
            tolerance,
            scee: torch.Tensor,
            all_exclusions_without_improper,
            dihedral_exclusions,
            periodic: bool,
            box: torch.Tensor):
        """
        Initialize the CommonEwald class.

        Parameters
        ----------
        charges : torch.Tensor
            Charges of the atoms.
        tolerance : float
            Tolerance for the Ewald summation, controlling accuracy.
        scee : torch.Tensor
            Scaling factor for 1-4 electrostatic interactions.
        all_exclusions_without_improper : list or torch.Tensor
            List of all exclusions without improper torsions.
        dihedral_exclusions : list or torch.Tensor
            List of dihedral (1-4) exclusions.
        periodic : bool
            Whether to use periodic boundary conditions.
        box : torch.Tensor
            Dimensions of the simulation box.
        """
        self.charges = charges
        self.scee = scee

        self.periodic = periodic
        self.box = box

        self.tolerance = tolerance
        self.all_exclusions = all_exclusions_without_improper
        self.dihedral_exclusions = dihedral_exclusions

        if self.dihedral_exclusions.size(0) > 0:
            self.one_four_charges = self.charges[self.dihedral_exclusions[0]] * self.charges[self.dihedral_exclusions[1]] / scee
        else:
            self.one_four_charges = torch.tensor([])

        self.sqrt_pi = math.sqrt(math.pi)

        self.alpha = None

    def ewald_exclusions(self, pos: torch.Tensor) -> torch.Tensor:
        """
        Calculate the Ewald exclusions energy.
        
        This method computes the correction to the electrostatic energy due to
        excluded atom pairs that would otherwise be counted in the reciprocal
        space calculation.

        Parameters
        ----------
        pos : torch.Tensor
            Positions of the atoms.

        Returns
        -------
        torch.Tensor
            Ewald exclusions energy.
        """
        distance_matrix = get_distances_edge_list(pos, self.all_exclusions, self.periodic, self.box)
        charges0 = self.charges[self.all_exclusions[0]]
        charges1 = self.charges[self.all_exclusions[1]]
        r_alpha_distance = torch.erf(distance_matrix * self.alpha)
        filter_r_alpha_distance_contrib = r_alpha_distance > 1e-6

        return -constants.ONE_BY_4_PI_EPSILON_0 * torch.sum(charges0[filter_r_alpha_distance_contrib] * charges1[filter_r_alpha_distance_contrib] / distance_matrix[filter_r_alpha_distance_contrib] *
                                                            r_alpha_distance[filter_r_alpha_distance_contrib]) + torch.sum(self.alpha * 2 / self.sqrt_pi * charges0[~filter_r_alpha_distance_contrib] * charges1[~filter_r_alpha_distance_contrib])

    def ewald_self(self, charges: torch.Tensor) -> torch.Tensor:
        """
        Calculate the Ewald self energy.
        
        This method computes the self-interaction correction term that arises
        from the interaction of each Gaussian charge distribution with itself.

        Parameters
        ----------
        charges : torch.Tensor
            Charges of the atoms.

        Returns
        -------
        torch.Tensor
            Ewald self energy.
        """
        return -torch.sum(charges * charges) * constants.ONE_BY_4_PI_EPSILON_0 * self.alpha / self.sqrt_pi

    def ewald_direct(
            self,
            inverse_dist: torch.Tensor,
            distance_matrix: torch.Tensor,
            neighborlist: torch.Tensor) -> torch.Tensor:
        """
        Calculate the Ewald direct space energy.
        
        This method computes the direct (real) space contribution to the
        electrostatic energy using a short-range cutoff.

        Parameters
        ----------
        inverse_dist : torch.Tensor
            Inverse distances between atom pairs.
        distance_matrix : torch.Tensor
            Distance matrix between atom pairs.
        neighborlist : torch.Tensor
            Neighbor list containing pairs of atoms within the cutoff.

        Returns
        -------
        torch.Tensor
            Ewald direct space energy.
        """
        pot = self.charges[neighborlist[0]] * self.charges[neighborlist[1]] * (torch.erfc(self.alpha * distance_matrix) * inverse_dist)
        return torch.sum(pot) * constants.ONE_BY_4_PI_EPSILON_0

    def electrostatic_energy_contribution_one_four(self, pos: torch.Tensor) -> torch.Tensor:
        """
        Calculate the electrostatic energy contribution for 1-4 interactions.
        
        This method computes the electrostatic energy for atom pairs that are
        separated by exactly three bonds (1-4 interactions), using the scaled
        charges.

        Parameters
        ----------
        pos : torch.Tensor
            Positions of the atoms.

        Returns
        -------
        torch.Tensor
            Electrostatic energy contribution for 1-4 interactions.
        """
        if self.dihedral_exclusions.size(0) == 0:
            return 0

        distance_matrix = get_distances_edge_list(pos, self.dihedral_exclusions, self.periodic, self.box)

        return torch.sum(self.one_four_charges / distance_matrix) * constants.ONE_BY_4_PI_EPSILON_0

    def ewald_reciprocal(self, pos):
        """
        Calculate the Ewald reciprocal space energy.
        
        This method must be implemented by subclasses to define the specific
        algorithm for computing the reciprocal space contribution.

        Parameters
        ----------
        pos : torch.Tensor
            Positions of the atoms.

        Raises
        ------
        NotImplementedError
            If the method is not implemented in the subclass.
        """
        raise NotImplementedError("Ewald reciprocal needs to implemented in specialized class")

    def calc_energy(
            self,
            pos: torch.Tensor,
            inverse_distance_cut: torch.Tensor,
            distance_matrix_cut: torch.Tensor,
            neighborlist: torch.Tensor) -> torch.Tensor:
        """
        Calculate the total Ewald energy.
        
        This method computes the total electrostatic energy by summing the
        direct space, reciprocal space, self-energy, exclusion, and 1-4
        interaction contributions.

        Parameters
        ----------
        pos : torch.Tensor
            Positions of the atoms.
        inverse_distance_cut : torch.Tensor
            Inverse distances within the cutoff.
        distance_matrix_cut : torch.Tensor
            Distance matrix within the cutoff.
        neighborlist : torch.Tensor
            Neighbor list.

        Returns
        -------
        torch.Tensor
            Total Ewald energy.
        """
        ewald_energy = self.ewald_direct(inverse_distance_cut, distance_matrix_cut, neighborlist)
        ewald_energy = ewald_energy + self.ewald_reciprocal(pos)
        ewald_energy = ewald_energy + self.ewald_self_energy
        ewald_energy = ewald_energy + self.ewald_exclusions(pos)
        one_four_energy = self.electrostatic_energy_contribution_one_four(pos)
        return ewald_energy + one_four_energy


class Ewald(CommonEwald):
    """
    Standard Ewald summation method for electrostatic interactions.
    
    This class implements the standard Ewald summation method, which splits
    the electrostatic calculation into direct space, reciprocal space, and
    self-energy contributions. It uses a Fourier-based approach for the
    reciprocal space calculation.

    Parameters
    ----------
    charges : torch.Tensor
        Charges of the atoms.
    tolerance : float
        Tolerance for the Ewald summation, controlling accuracy.
    cutoff : float
        Cutoff distance for the direct space calculation.
    scee : torch.Tensor
        Scaling factor for 1-4 electrostatic interactions.
    all_exclusions_without_improper : list or torch.Tensor
        List of all exclusions without improper torsions.
    dihedral_exclusions : list or torch.Tensor
        List of dihedral (1-4) exclusions.
    periodic : bool
        Whether to use periodic boundary conditions.
    box : torch.Tensor
        Dimensions of the simulation box.
    k_space_use_sphere : bool, optional
        Whether to use a spherical cutoff in k-space. Default is False.
        
    Attributes
    ----------
    sqrt_pi : float
        Square root of pi.
    two_pi : float
        2 * pi.
    dtype : torch.dtype
        Data type of the charges.
    k_space_use_sphere : bool
        Whether a spherical cutoff is used in k-space.
    alpha : float
        Ewald parameter, controlling the split between direct and reciprocal space.
    k_meshgrid : torch.Tensor
        Mesh grid of k-vectors for reciprocal space calculation.
    k_meshgrid_sq : torch.Tensor
        Squared magnitudes of k-vectors.
    sym_factor : torch.Tensor
        Symmetry factors for k-vectors.
    k_factor : torch.Tensor
        Precomputed factors for k-vectors.
    norm : torch.Tensor
        Normalization factor for reciprocal space calculation.
    ewald_self_energy : torch.Tensor
        Self-energy contribution.
    """

    @staticmethod
    def find_zero(function, intial_guess=10):
        """
        Find the zero of a function using a simple search algorithm.
        
        This method finds the value where the given function crosses zero,
        starting from an initial guess and moving in the appropriate direction.

        Parameters
        ----------
        function : callable
            Function to find the zero of.
        intial_guess : int, optional
            Initial guess for the zero. Default is 10.

        Returns
        -------
        int
            Value where the function crosses zero.
        """
        current_arg = intial_guess
        value = function(current_arg)
        if value > 0.0:
            while value > 0 and current_arg > 0:
                current_arg -= 1
                value = function(current_arg)
            return current_arg + 1

        while value < 0.0:
            current_arg += 1
            value = function(current_arg)
        return current_arg

    def ewald_error(self, k_max: int, box_length: torch.Tensor, alpha: torch.Tensor, tolerance: float) -> torch.Tensor:
        """
        Calculate the Ewald error for a given k_max value.
        
        This method estimates the error in the reciprocal space calculation
        for a given maximum k-vector magnitude.

        Parameters
        ----------
        k_max : int
            Maximum k value to consider.
        box_length : torch.Tensor
            Length of the simulation box.
        alpha : torch.Tensor
            Ewald parameter.
        tolerance : float
            Desired tolerance for the calculation.

        Returns
        -------
        torch.Tensor
            Difference between the estimated error and the desired tolerance.
        """
        temp = k_max * torch.pi / (box_length * alpha)
        return tolerance - 0.05 * torch.sqrt(box_length * alpha) * k_max * torch.exp(-temp ** 2)

    def update_box(self, box):
        """
        Update the simulation box dimensions.
        
        This method updates the box dimensions and recalculates the k-vectors
        for the reciprocal space calculation.
        
        Parameters
        ----------
        box : torch.Tensor
            The new box dimensions.
        """
        super().update_box(box)
        self.calc_k_vectors()

    def __init__(
            self,
            charges: torch.Tensor,
            tolerance: float,
            cutoff: float,
            scee: torch.Tensor,
            all_exclusions_without_improper,
            dihedral_exclusions,
            periodic: bool,
            box: torch.Tensor,
            k_space_use_sphere: bool = False) -> None:
        """
        Initialize the Ewald class.

        Parameters
        ----------
        charges : torch.Tensor
            Charges of the atoms.
        tolerance : float
            Tolerance for the Ewald summation, controlling accuracy.
        cutoff : float
            Cutoff distance for the direct space calculation.
        scee : torch.Tensor
            Scaling factor for 1-4 electrostatic interactions.
        all_exclusions_without_improper : list or torch.Tensor
            List of all exclusions without improper torsions.
        dihedral_exclusions : list or torch.Tensor
            List of dihedral (1-4) exclusions.
        periodic : bool
            Whether to use periodic boundary conditions.
        box : torch.Tensor
            Dimensions of the simulation box.
        k_space_use_sphere : bool, optional
            Whether to use a spherical cutoff in k-space. Default is False.
        """
        super().__init__(charges, tolerance, scee, all_exclusions_without_improper, dihedral_exclusions, periodic, box)

        self.sqrt_pi = math.sqrt(math.pi)
        self.two_pi = 2 * torch.pi
        self.dtype = charges.dtype
        self.k_space_use_sphere = k_space_use_sphere

        self.alpha = math.sqrt(-math.log(2.0 * self.tolerance)) / cutoff
        self.calc_k_vectors()
        self.ewald_self_energy = self.ewald_self(charges)

    def calc_k_vectors(self):
        """
        Calculate the k-vectors for the reciprocal space calculation.
        
        This method determines the appropriate k-vectors based on the box dimensions,
        Ewald parameter, and tolerance, and precomputes factors needed for the
        reciprocal space calculation.
        """
        k_max = []
        for bo in self.box:
            def specialized_ewald_error(x): return self.ewald_error(x, bo, self.alpha, self.tolerance)
            k_max_val = self.find_zero(specialized_ewald_error)
            if k_max_val % 2 == 0:
                k_max_val += 1
            k_max.append(k_max_val)

        max_k_max = torch.max(torch.tensor(k_max))
        for i, _ in enumerate(k_max):
            k_max[i] = max_k_max

        # Define k values
        kx_vals = torch.arange(0, k_max[0], 1) * self.two_pi / self.box[0]
        ky_vals = torch.arange(-k_max[1] + 1, k_max[1], 1) * self.two_pi / self.box[1]
        kz_vals = torch.arange(-k_max[2] + 1, k_max[2], 1) * self.two_pi / self.box[2]

        # Create a 3D meshgrid of all combinations
        kx_grid, ky_grid, kz_grid = torch.meshgrid(
            kx_vals, ky_vals, kz_vals, indexing='ij')
        k_meshgrid = torch.stack([kx_grid, ky_grid, kz_grid], dim=-1)
        k_meshgrid_sq = k_meshgrid.square().sum(-1)

        # Remove combinations where (kx**2 + ky**2 + kz**2) == 0
        mask = k_meshgrid_sq > 0.0

        # Remove combinations where (kx**2 + ky**2 + kz**2) > k_max**2
        if self.k_space_use_sphere:
            k_max_sq = max(k_max[0] / self.box[0], k_max[1] / self.box[1], k_max[2] / self.box[2]) * self.two_pi
            mask = mask & (k_meshgrid_sq <= k_max_sq)

            # Apply the mask to filter valid grid points and corresponding factors
        self.k_meshgrid = k_meshgrid[mask]
        self.k_meshgrid_sq = k_meshgrid_sq[mask]

        # Cast to dtype
        self.k_meshgrid = self.k_meshgrid.to(self.dtype)
        self.k_meshgrid_sq = self.k_meshgrid_sq.to(self.dtype)

        # Calculate the factor arising through removing the summation via the trigonometric identity
        self.sym_factor = torch.where(self.k_meshgrid[:, 0] == 0.0, 1.0, 2.0)

        # Compute the rest as in the original code
        factor_ewald = -1.0 / 4.0 / self.alpha / self.alpha
        self.k_factor = torch.exp(factor_ewald * self.k_meshgrid_sq) / self.k_meshgrid_sq

        self.norm = self.two_pi * constants.ONE_BY_4_PI_EPSILON_0 / torch.prod(self.box)

    def ewald_reciprocal(self, pos: torch.Tensor) -> torch.Tensor:
        """
        Calculate the Ewald reciprocal space energy.
        
        This method computes the reciprocal space contribution to the
        electrostatic energy using a Fourier-based approach.

        Parameters
        ----------
        pos : torch.Tensor
            Positions of the atoms.

        Returns
        -------
        torch.Tensor
            Ewald reciprocal space energy.
        """
        # kr has shape: n_atoms x k_max
        kr = torch.einsum('ai, ki -> ak', pos, self.k_meshgrid)

        # real and imaginary parts of the structure factor have shape: k_max
        structure_factor_real = torch.sum(self.charges.unsqueeze(-1) * torch.cos(kr), dim=0)
        structure_factor_imag = torch.sum(self.charges.unsqueeze(-1) * torch.sin(kr), dim=0)

        # square of the structure factor has shape: k_max
        sfactor_sq = structure_factor_real ** 2 + structure_factor_imag ** 2

        energy_sum = torch.sum(self.k_factor * sfactor_sq * self.sym_factor)

        return energy_sum * self.norm


class PME(CommonEwald):
    """
    Particle Mesh Ewald (PME) method for electrostatic interactions.
    
    This class implements the Particle Mesh Ewald method, which is an efficient
    algorithm for computing electrostatic interactions in periodic systems.
    PME uses a grid-based approach with B-splines for charge assignment and
    Fast Fourier Transforms (FFTs) for the reciprocal space calculation.

    Parameters
    ----------
    charges : torch.Tensor
        Charges of the atoms.
    tolerance : float
        Tolerance for the PME summation, controlling accuracy.
    cutoff : float
        Cutoff distance for the direct space calculation.
    scee : torch.Tensor
        Scaling factor for 1-4 electrostatic interactions.
    all_exclusions_without_improper : list or torch.Tensor
        List of all exclusions without improper torsions.
    dihedral_exclusions : list or torch.Tensor
        List of dihedral (1-4) exclusions.
    periodic : bool
        Whether to use periodic boundary conditions.
    box : torch.Tensor
        Dimensions of the simulation box.
    order : int, optional
        Order of the B-spline interpolation. Default is 5.
        
    Attributes
    ----------
    order : int
        Order of the B-spline interpolation.
    alpha : float
        Ewald parameter, controlling the split between direct and reciprocal space.
    ngrid : torch.Tensor
        Number of grid points in each dimension.
    box_here : torch.Tensor
        Box dimensions as a diagonal matrix.
    rbox : torch.Tensor
        Inverse of the box matrix.
    Vol : torch.Tensor
        Volume of the simulation box.
    BC : torch.Tensor
        Precomputed B and C matrices for the PME calculation.
    ewald_self_energy : torch.Tensor
        Self-energy contribution.
    """

    def update_box(self, box):
        """
        Update the simulation box dimensions.
        
        This method updates the box dimensions and recalculates the matrices
        needed for the PME calculation.
        
        Parameters
        ----------
        box : torch.Tensor
            The new box dimensions.
        """
        super().update_box(box)
        self.box_here = torch.diag(box)
        self.rbox = torch.linalg.inv(self.box_here)  # This only works for orthorombic boxes.
        self.Vol = torch.linalg.det(self.box_here)

        self.BC = self.create_BC()

    def __init__(
            self,
            charges: torch.Tensor,
            tolerance,
            cutoff,
            scee: torch.Tensor,
            all_exclusions_without_improper,
            dihedral_exclusions,
            periodic: bool,
            box: torch.Tensor,
            order=5):
        """
        Initialize the PME class.

        Parameters
        ----------
        charges : torch.Tensor
            Charges of the atoms.
        tolerance : float
            Tolerance for the PME summation, controlling accuracy.
        cutoff : float
            Cutoff distance for the direct space calculation.
        scee : torch.Tensor
            Scaling factor for 1-4 electrostatic interactions.
        all_exclusions_without_improper : list or torch.Tensor
            List of all exclusions without improper torsions.
        dihedral_exclusions : list or torch.Tensor
            List of dihedral (1-4) exclusions.
        periodic : bool
            Whether to use periodic boundary conditions.
        box : torch.Tensor
            Dimensions of the simulation box.
        order : int, optional
            Order of the B-spline interpolation. Default is 5.
        """
        super().__init__(charges, tolerance, scee, all_exclusions_without_improper, dihedral_exclusions, periodic, box)
        self.order = order

        self.alpha = (1.0 / cutoff) * math.sqrt(-math.log(2.0 * self.tolerance))
        self.ngrid = torch.ceil(2 * self.alpha * self.box / (3 * math.pow(self.tolerance, 0.2))).to(torch.int32)

        self.box_here = torch.diag(box)
        self.rbox = torch.linalg.inv(self.box_here)  # This only works for orthorombic boxes.
        self.Vol = torch.linalg.det(self.box_here)

        self.BC = self.create_BC()
        self.ewald_self_energy = self.ewald_self(charges)

    def create_BC(self):
        """
        Create the B and C matrices for PME.
        
        This method computes the B-spline coefficients and the C matrix
        needed for the PME reciprocal space calculation.

        Returns
        -------
        torch.Tensor
            Combined B and C matrices for PME.
        """
        # Compute the grid
        K1, K2, K3 = self.ngrid
        maxK1 = (K1 + 1) // 2
        maxK2 = (K2 + 1) // 2
        maxK3 = (K3 + 1) // 2
        _x, _y, _z = torch.arange(K1), torch.arange(K2), torch.arange(K3)
        mx = ((_x + maxK1) % K1 - maxK1).unsqueeze(1).unsqueeze(2)  # [nx, 1, 1]
        my = ((_y + maxK2) % K2 - maxK2).unsqueeze(0).unsqueeze(2)  # [1, ny, 1]
        mz = ((_z + maxK3) % K3 - maxK3).unsqueeze(0).unsqueeze(0)  # [1, 1, nz]
        mhx = mx * self.rbox[0][0]  # Shape [nx, 1, 1]
        mhy = mx * self.rbox[1][0] + my * self.rbox[1][1]  # Shape [nx, ny, 1]
        mhz = mx * self.rbox[2][0] + my * self.rbox[2][1] + mz * self.rbox[2][2]  # Shape [nx, ny, nz]
        m2 = mhx * mhx + mhy * mhy + mhz * mhz  # Shape [nx, ny, nz]
        m2[0, 0, 0] = 1e-12  # to avoid numerical problems... C is set zero later anyways

        # Compute C
        boxfactor = (torch.pi * self.Vol)
        factor = (math.pi**2 / (self.alpha**2))
        C = constants.ONE_BY_4_PI_EPSILON_0 * torch.exp(-factor * m2) / boxfactor / m2
        C[0, 0, 0] = 0

        # Compute B
        Kmax = max(K1, K2, K3)
        _data = torch.zeros(Kmax)
        _k = torch.arange(self.order).unsqueeze(0)
        _data[:self.order] = bspline(_k + 1, self.order)
        Bs = []
        for d in range(3):
            i_indices = torch.arange(self.ngrid[d])
            j_indices = torch.arange(self.ngrid[d]).unsqueeze(1)
            angles = (2.0 * torch.pi * j_indices * i_indices) / self.ngrid[d]
            cos_vals = torch.cos(angles)
            sin_vals = torch.sin(angles)

            sc = torch.matmul(_data[:self.ngrid[d]], cos_vals)
            ss = torch.matmul(_data[:self.ngrid[d]], sin_vals)
            Bi = sc * sc + ss * ss
            mask = Bi < 1.0e-7
            if mask.any():
                Bi[mask] = (Bi.roll(shifts=-1)[mask] + Bi.roll(shifts=1)[mask]) / 2
            Bs += [Bi]

        B = Bs[0][:, None, None] * Bs[1][None, :, None] * Bs[2][None, None, :]
        BC = C / B
        return BC

    def ewald_reciprocal(self, position):
        """
        Calculate the Ewald reciprocal space energy for PME.
        
        This method computes the reciprocal space contribution to the
        electrostatic energy using the Particle Mesh Ewald approach with
        B-splines and Fast Fourier Transforms.

        Parameters
        ----------
        position : torch.Tensor
            Positions of the atoms.

        Returns
        -------
        torch.Tensor
            Ewald reciprocal space energy for PME.
        """

        # Scaled fractional positions
        y = position @ self.rbox * self.ngrid
        # integer positions
        y_ = y.to(torch.int32)
        # fraction on the box
        fr = y - y_
        # id of the box
        idx = y_ % self.ngrid
        # expand dimension
        fr = fr.unsqueeze(-1)
        y = y.unsqueeze(-1)

        _g = torch.arange(self.order)
        M1 = bspline(fr[:, 0] + _g, self.order).flip(1)
        M2 = bspline(fr[:, 1] + _g, self.order).flip(1)
        M3 = bspline(fr[:, 2] + _g, self.order).flip(1)
        Q123 = self.charges[:, None, None, None] * (M1[:, :, None, None] * M2[:, None, :, None] * M3[:, None, None, :])

        x_indices = (idx[:, 0, None] + _g) % self.ngrid[0]
        y_indices = (idx[:, 1, None] + _g) % self.ngrid[1]
        z_indices = (idx[:, 2, None] + _g) % self.ngrid[2]

        x_flat = x_indices[:, :, None, None].expand(-1, -1, self.order, self.order)
        y_flat = y_indices[:, None, :, None].expand(-1, self.order, -1, self.order)
        z_flat = z_indices[:, None, None, :].expand(-1, self.order, self.order, -1)

        Q = torch.zeros((self.ngrid[0], self.ngrid[1], self.ngrid[2]), dtype=position.dtype)
        Q = torch.index_put(Q, (x_flat, y_flat, z_flat), Q123, accumulate=True)

        fQ = torch.fft.fftn(Q)

        Qstar = fQ.real * fQ.real + fQ.imag * fQ.imag

        Esq = self.BC * Qstar
        energy = 0.5 * torch.sum(Esq)

        return energy.real
